static double _axx,_axy,_ayy;
static double _bx,_by,_c;
static bool isInitialized = false;

const double interp_scatt_bilinear(const double x,
				   const double y)
{
    if (!isInitialized)
    {
        fprintf (stderr, "Scattered bilinear interpolation coefficients have not been set. Use init_scatt_bilinear function to initialize the interpolation. \n");
	exit (EXIT_FAILURE);
    }
    return (x*(_axx*x + _axy*y + _bx) + y*(_ayy*y + _by) +_c);
}

const double scatt_bilinear_derivative_x(const double x,
					 const double y)
{
    if (!isInitialized)
    {
        fprintf (stderr, "Scattered bilinear interpolation coefficients have not been set. Use init_scatt_bilinear function to initialize the interpolation. \n");
	exit (EXIT_FAILURE);
    }
    return (2.*_axx*x + _axy*y + _bx);
}

const double scatt_bilinear_derivative_y(const double x,
					 const double y)
{
    if (!isInitialized)
    {
        fprintf (stderr, "Scattered bilinear interpolation coefficients have not been set. Use init_scatt_bilinear function to initialize the interpolation. \n");
	exit (EXIT_FAILURE);
    }
    return (2.*_ayy*y + _axy*x + _by);
}

    
const double scatt_bilinear_derivative(const double x,
				       const double y,
				       const int dir)
{
    if (!isInitialized)
    {
        fprintf (stderr, "Scattered bilinear interpolation coefficients have not been set. Use init_scatt_bilinear function to initialize the interpolation. \n");
	exit (EXIT_FAILURE);
    }
    if (dir==0)
	return scatt_bilinear_derivative_x(x, y);
    else if (dir==1)
	return scatt_bilinear_derivative_y(x, y);
    else
    {
        fprintf (stderr, "Coordinate index is out of the range for scattered bilinear interpopation function. \n");
	exit (EXIT_FAILURE);
    }
}


